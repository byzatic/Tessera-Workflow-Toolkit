package io.github.byzatic.tessera.storageapi.exceptions;

public class MCg3ApiOperationIncompleteException extends Exception {

    public MCg3ApiOperationIncompleteException(String message) {
        super(message);
    }

    public MCg3ApiOperationIncompleteException(Throwable cause) {
        super(cause);
    }

    public MCg3ApiOperationIncompleteException(String message, Throwable cause) {
        super(message, cause);
    }

    public MCg3ApiOperationIncompleteException(Throwable cause, String message) {
        super(message, cause);
    }

}
